package mk.finki.ukim.aud3;

public class TwoSmallest {
    private int a;
    private int b;

    public TwoSmallest(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public TwoSmallest() {
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
